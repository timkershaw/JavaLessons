package com.lessons;

// variable is a place holder for a value that behaves as the value it contains
public class Variables {

    public static void main(String[] args) {
        /*
    int x;   declaration
    x = 123; assignment
    int x = 123; initialization
     */

        int x = 123;
        double y = 3.14;
        boolean z = true;
        char symbol = '@';

        //String starts with a capitol S because it is a reference data type
        String name = "Tim";

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
        System.out.println(symbol);
        System.out.println(name);
    }



}
