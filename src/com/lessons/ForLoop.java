package com.lessons;

public class ForLoop {
    public static void main(String[] args) {
        // for loop = executes a block of code a limited amount of times

        //create a program that will count from 0-10

        //first part is declared i
        //second part interates the for loop as long as the condition is true
        //the third statement increments or decrements the index
        for(int i = 10; i>=0; i--) {
            System.out.println(i);
        }
        System.out.println("Happy New Year!");
    }
}
